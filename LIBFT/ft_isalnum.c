#include "libft.h"

int	ft_isalnum(int ch)
{
	if ((ch <= 122 && ch >= 97) || (ch <= 90 && ch >= 65)
		|| (ch <= 57 && ch >= 48))
	{
		return (ch);
	}
	else
	{
		return (0);
	}
}
